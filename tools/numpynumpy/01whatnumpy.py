import numpy as np# 导入numpy，通常为了简便，重命名为np（在本文件内有效）

array = np.array([[1,3,2],[6,5,4],[7,9,8]]) # 定义一个矩阵
print(array)
# [[1 3 2]
#  [6 5 4]
#  [7 9 8]]

# 计算矩阵array的行列式
print(np.linalg.det(array))
# -18.000000000000004

# 计算矩阵array的逆矩阵
print(np.linalg.inv(array))
# [[-0.22222222  0.33333333 -0.11111111]
#  [ 1.11111111  0.33333333 -0.44444444]
#  [-1.05555556 -0.66666667  0.72222222]]